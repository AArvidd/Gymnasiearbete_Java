public class Sphere {

    private float X;
    private float Y;
    private float Z;

    private float R;

    public Sphere(float X, float Y, float Z, float R){
        this.X = X;
        this.Y = Y;
        this.Z = Z;

        this.R = R;
    }

    public float getX() {
        return X;
    }
    public float getY() {
        return Y;
    }
    public float getZ() {
        return Z;
    }
    public float getR() {
        return R;
    }
}
